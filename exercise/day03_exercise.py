# 3.输入任意一个字符串，判断这个字符串是否是回文
#   回文是指中心对称的文字，如:
#   上海自来水来自海上
#   ABCCBA
#   12321
print('===================================Day03练习题第三题===================================\n\n')
s3=input("请输入一个字符串：")
mid=len(s3)//2
print('您输入的字符串是回文' if s3[:mid]==s3[-1:(len(s3)-mid-1):-1] else '您输入的字符串不是回文')


