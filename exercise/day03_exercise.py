# 6.输入三行文字，让这三行文字在一个方框居中显示
#   如输入(不要输入中文):
#   hello tarena!
#   my name is weimingze
#   python
#   显示如下:
#   +----------------------+
#   |    hello tarena!     |
#   | my name is weimingze |
#   |        python        |
#   +----------------------+
print('请输入三行文字(不要输入中文)：')
s6_1=input('请输入第一行文字：')
s6_2=input('请输入第二行文字：')
s6_3=input('请输入第三行文字：')
len_1=len(s6_1)
len_2=len(s6_2)
len_3=len(s6_3)
max_value=max(len_1,len_2,len_3)
mid_1=(max_value-len_1)//2
mid_2=(max_value-len_2)//2
mid_3=(max_value-len_3)//2
print(' '*mid_1+s6_1)
print(' '*mid_2+s6_2)
print(' '*mid_3+s6_3)



