# 2.练习:
#   写程序，输入一段字符串，打印出这个字符串中出现过的字符及出现过的次数:
#     如:
#       输入: ABCDABCABA
#     输出:
#       A : 4次
#       C : 2次
#       B : 3次
#       D : 1次
#     注: 不要求打印的顺序
d2 = {}
s2 = input("请输入一个符串：")
for ch in s2:
	if ch not in d2:
		d2[ch] = 1
	else:
		d2[ch] = d2[ch] + 1
else:
	for s2_key in d2:
		print(s2_key,"%d次" % d2[s2_key],sep="：")
	
